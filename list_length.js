/*
Дан массив строк
"" "а" "аб" "абв" "абвг" "абвгд" "абвгде"
проверьте чтобы длина строки соответствовала 
порядковому номеру в массиве
*/
const list = ["", "а", "аб", "абв", "абвг", "абвгд", "абвгде"];
for (let i = 0; i < list.length; i++) {
    if (list[i].length != i) {
        console.log ("Error");
        break;
    }
}